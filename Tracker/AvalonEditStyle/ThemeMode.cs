namespace Tracker.AvalonEditStyle
{
    /// <summary>
    /// Specifies the mode of the theme to use.
    /// </summary>
    public enum ThemeMode
    {
        /// <summary>
        /// The Blue theme was the most popular theme for Visual Studio
        /// back in 2012-2017.
        /// </summary>
        Blue = 0,

        /// <summary>
        /// The Light theme provides colors mostly being white and white smoke.
        /// </summary>
        Light = 1,

        /// <summary>
        /// The Dark theme is currently the most popular theme for Visual Studio.
        /// </summary>
        Dark = 2,
    }
}
